<h1> Past Meetings and Events </h1>


<p><b>SC2002</b>
<br>The Globus Project was involved in many ways at SC2002 in Baltimore, 
November 16-22, 2002. 


<p><b>Edinburgh Tutorials</a></b>
<br>The Globus Project, in conjunction with the U.K. eScience Institute, presented four days of tutorials in Edinburgh in January 2002.</p>
    
<p><b>Chicago Tutorials</b>
<br>The Globus Project presented five days of tutorials at Argonne National Laboratory in Chicago, Illinois in late January 2002.</p>
    
<p><b>SC2001</b>
<br>At SC2001 (November 10-16), the Globus Project showed recent work on Grid applications built using the Globus Toolkit, released the Globus Toolkit 2.0, and demonstrated our recent work on advanced Grid technologies.  During the conference, the Globus Toolkit was demonstrated in action by numerous application demonstrations around the exhibit hall.  The Globus Project was also represented by paper presentations, tutorials, posters, SC Global presentations and panels, and other conference activities.</p>
    
<p><b>Introduction to Grid Computing and the Globus Toolkit</b>
<br> A practical introduction both to Grid computing, and the Globus Toolkit was held Friday, October 12, 9:30am - 5:30pm, at the e-Science Institute, 15 South College Street, Edinburgh, Scotland. </p>
    
<p><b><a href="http://www.informatica.unile.it/laboratori/lab-hpc/euroglobus/index.htm">EuroGlobus</a></b>
<br> The first EuroGlobus conference was held June 16-23, 2001 in Lecce, Italy.</p>

<p><b>Globus Retreat 2001</b>
<br> The Globus Retreat was held in August in San Francisco. Like previous meetings in this series, this event was an opportunity for users and developers of the Globus Toolkit to discuss common interests, learn about recent developments, and plan future directions.</p>

<p><b><a href="http://www-sop.inria.fr/JGI2001/">Joint ACM Java Grande/ISCOPE 2001 Conference</a></b>
<br>This full day tutorial--introducing JavaGrande activities--included details on Java-based Grid programming using Globus services. The tutorial took place on June 2 in San Francisco during the Java One conference.</p>

<p><b><a href="http://www.ippsxx.org/ipdps2001/2001_tutorial4.html">IPDPS 2001</a></b>
<br>We provided a tutorial on grid computing, Globus, and Java interfaces to the Grid at IPDPD 2001 in San Francisco, April 27th, 2001.</p>

<p><b>Supercomputing 2000</b>
<br>Supercomputing 2000 was held on November 4-10, 2000 at the Dallas Convention Center. The Globus Project demonstrated our work on data grid technologies and applications that use these technologies.</p>

<p><b><a href="http://www.isi.edu/info-services/">Information Services Workshop</a></b>
<br>Our Information Services Workshop was held at NASA Ames Research Center on September 21-22, 2000. The workshop provided a forum for discussing current experiences and new approaches used to provide the necessary information to the Grid computing infrastructure. We discussed various issues pertaining to the discovery, acquisition and dissemination of Grid-related information. [<a href="http://www.isi.edu/info-services/agenda.html">slides</a>]</p>

<p><b>Globus Retreat 2000</b>
<br>The 2000 Globus Retreat was held July 30th through August 1st in Pittsburgh, PA. These were the three days before the IEEE High Performance Distributed Computing (HPDC) symposium. (See the <a href="http://www.cs.cmu.edu/~hpdc/">HPDC website</a>.) This retreat provided an opportunity for users and developers of the Globus Toolkit to discuss common interests. </p>

<p><b>Globus Tutorials (U.K.)</b>
<br>Rutherford Appleton Laboratory in the United Kingdom hosted a two-day Globus Tutorial on June 21-22, 2000. These tutorials covered basic grid concepts, Globus Toolkit components, and details for programmers using the Globus Toolkit.</p>

<p><b>Fifth Globus Retreat: August 1-3, 1999, Redondo Beach, CA</b>
<br>The fifth Globus retreat was held on August 1-3 in Redondo Beach, CA, the three days before the IEEE High Performance Distributed Computing symposium, which should also be of interest (see <a href="http://www.isi.edu/hpdc8/">http://www.isi.edu/hpdc8/</a>). These events provided an opportunity for users and developers of the Globus distributed computing toolkit to discuss common interests. <br>
    The program included: 
<ul>
   <li>Tutorials on Globus programming and Globus system administration, on Sunday at ISI</li>
   <li>Technical program on Monday and Tuesday, including talks by Globus users and updates on Globus status </li>
   <li>Discussion of future directions </li>
</ul>
   
 <p><b>Fourth Globus Retreat: August 2-4, 1998, Chicago</b>
<br> The fourth Globus retreat was held August 2-4 in Chicago, the week after the IEEE High Performance Distributed Computing symposium (<a href="http://www.mcs.anl.gov/hpdc7">HPDC-7</a>), which should also be of interest. These events provide an opportunity for users and developers of the Globus distributed computing toolkit to discuss common interests.</p>
