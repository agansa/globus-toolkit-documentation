<p><b>December 4 &amp; 5, 1996, San Diego</b>
<br>The second Globus retreat was held at the San Diego Supercomputer Center. The primary purpose of the meeting was to present and review basic Globus services and associated APIs (for which specifications are available), and the GUSTO testbed on which these services are being deployed. Status reports were also given on a number of related projects. Thanks a lot for the San Diego Supercomputer Center, and in particular to Charlotte Smart and Reagan Moore, for hosting the meeting.</p>
