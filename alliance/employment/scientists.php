<h1 class="first">Scientific/Engineering Staff</h1>

<p>The Globus Project employs scientists and engineers with outstanding records of 
accomplishment to lead explorations into applied Computer and Computational 
Science.  Members of the scientific staff define 
<a href="../../research/"></a>research programs and lead teams of researchers, 
programmers, students, and externl partners.

<p>All of our member institutions foster highly collaborative, synergistic relationships
between researchers, programmers, external partners. and students. We strongly 
encourage scientists to develop their own avenues of discovery while working in 
partnership with other researchers both within and outside our group.  
Our identity as "the Globus Project" is defined by the fact that 
our individual specialties all contribute in some way toward solving problems 
in the field of Grid computing.</p>

<p>Scientific staff should have a PhD in computer science or a
related field and a strong record of creative achievement in computer systems
research.</p>

