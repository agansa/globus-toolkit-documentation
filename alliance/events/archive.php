<p><b>SC2001</b>
<br>At SC2001 (November 10-16), the Globus Project showed recent work on Grid applications built using the Globus Toolkit, released the Globus Toolkit 2.0, and demonstrated our recent work on advanced Grid technologies.  During the conference, the Globus Toolkit was demonstrated in action by numerous application demonstrations around the exhibit hall.  The Globus Project was also represented by paper presentations, tutorials, posters, SC Global presentations and panels, and other conference activities.</p>
    
<p><b>Introduction to Grid Computing and the Globus Toolkit</b>
