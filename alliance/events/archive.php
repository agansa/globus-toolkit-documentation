<p><b>Supercomputing 2000</b>
<br>Supercomputing 2000 was held on November 4-10, 2000 at the Dallas Convention Center. The Globus Project demonstrated our work on data grid technologies and applications that use these technologies.</p>

<p><b><a href="http://www.isi.edu/info-services/">Information Services Workshop</a></b>
<br>Our Information Services Workshop was held at NASA Ames Research Center on September 21-22, 2000. The workshop provided a forum for discussing current experiences and new approaches used to provide the necessary information to the Grid computing infrastructure. We discussed various issues pertaining to the discovery, acquisition and dissemination of Grid-related information. [<a href="http://www.isi.edu/info-services/agenda.html">slides</a>]</p>

<p><b>Globus Retreat 2000</b>
<br>The 2000 Globus Retreat was held July 30th through August 1st in Pittsburgh, PA. These were the three days before the IEEE High Performance Distributed Computing (HPDC) symposium. (See the <a href="http://www.cs.cmu.edu/~hpdc/">HPDC website</a>.) This retreat provided an opportunity for users and developers of the Globus Toolkit to discuss common interests. </p>

<p><b>Globus Tutorials (U.K.)</b>
