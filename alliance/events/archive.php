<p><b>Globus Retreat 2001</b>
<br> The Globus Retreat was held in August in San Francisco. Like previous meetings in this series, this event was an opportunity for users and developers of the Globus Toolkit to discuss common interests, learn about recent developments, and plan future directions.</p>

<p><b><a href="http://www-sop.inria.fr/JGI2001/">Joint ACM Java Grande/ISCOPE 2001 Conference</a></b>
<br>This full day tutorial--introducing JavaGrande activities--included details on Java-based Grid programming using Globus services. The tutorial took place on June 2 in San Francisco during the Java One conference.</p>

<p><b><a href="http://www.ippsxx.org/ipdps2001/2001_tutorial4.html">IPDPS 2001</a></b>
