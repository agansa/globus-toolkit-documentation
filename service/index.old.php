<h1 class="first">Globus.org Data Movement Services (beta)</h1>
<p>DataKoa is becoming Globus.org.</p>
<p>
One principle guiding globus.org development is that features must be easy for non-experts to use and integrate 
into higher-level systems. Thus globus.org will support a variety of interfaces in common use. The public interfaces 
for this release include a command-line interface and simple Web browser interface. A REST interface is planned 
for a future release.
</p>
<p>
Another guiding principle is that globus.org should shield users from
technology problems outside of their control. As documented in a recent
<a href="http://www.mcs.anl.gov/%7Echilders/perspectives/">user experience study</a>,
wide-area data transfer can be a laborious and painful process, in part
due to infrastructure failures and similar factors. Globus.org mitigates
these problems so users can spend their time on domain-specific work
instead of failure management.
</p>
