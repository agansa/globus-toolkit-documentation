<p>
Globus.org delivers high-performance, reliable data movement services to end users without requiring construction of custom, end-to-end systems. Implemented as an online, hosted service, Globus.org makes the same robust file transfer capabilities traditionally available only on the largest scale grid implementations, accessible to users that do not have the benefit of well-funded, technically savvy IT organizations.
</p>

<h2>Globus.org advantages</h2>
<p><b>Simplicity.</b> 
Globus.org is built on the premise that end users should not have to be technology experts in order to share data among multiple sites. Setup is as simple as logging into the Globus.org service Web console and identifying the data to be transferred between two or more endpoints. Execution is via straightforward command line and Web interfaces.
</p>

<p><b>Reliability.</b>
Dealing with data movement failures is perhaps the most frustrating aspect of distributed data management. Globus.org reliably manages file transfer requests between two endpoints, transparently handling transient errors by re-trying failed transfers. The service allows users to focus on domain-specific work instead of failure management, but detailed logs are maintained and available for users to query the error messages of failed transfers.
</p>

<p><b>Control.</b>
Globus.org provides configurable levels of control over transfer requests. End users can monitor the status of all data movement requests via a Web management console or the command line. Summary progress information is provided, and users can examine details of individual transfers and cancel requests in progress.
</p>
<p><b>Performance.</b>
Globus.org takes advantage of tried and tested Globus software components, such as GridFTP, to deliver unparalleled performance, and leverages commercial platforms such as those provided by Amazon Web Services and Google to ease creation and operation of the service.
</p>


<h2>Software-as-a-Service Model Delivers Advanced Capabilities</h2>
<p>
Globus.org is a newly created online, hosted service (i.e. Software-as-a-Service), providing higher-level, end-to-end grid capabilities to end users, as well as grid builders looking for more complete solutions to build upon.
</p>
<p>
One of the gaps identified by users of the Globus Toolkit is the lack of pre-packaged, end-to-end functionality that can be easily accessed outside the confines of a custom, domain-specific grid system. End users often struggle to assemble and use the many disjoint capabilities provided to them by various resource providers as they try to make effective use of remote computing systems, data, and other online services. Starting with reliable data movement, Globus.org aims to deliver multiple such services by leveraging many of the tried and tested components available in the Globus Toolkit. In addition to addressing the complexity of assembling end-to-end services, Globus.org provides the support necessary for end users to rapidly resolve operational problems encountered when dealing with distributed resources that are not under the user's direct control.
</p>
